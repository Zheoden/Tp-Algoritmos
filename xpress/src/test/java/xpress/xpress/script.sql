
DROP TABLE usuario;
CREATE TABLE usuario (id_usuario INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1) PRIMARY KEY
                           , username VARCHAR(255), password VARCHAR(255),id_persona INTEGER);

DROP TABLE aplicacion;
CREATE TABLE aplicacion (id_aplicacion INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1) PRIMARY KEY
                      , descripcion VARCHAR(255));
                      
DROP TABLE rol;                      
CREATE TABLE rol(id_rol INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1) PRIMARY KEY
                          , descripcion VARCHAR(255));

DROP TABLE persona;
CREATE TABLE persona (id_persona INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1) PRIMARY KEY
					, nombre VARCHAR(255)
					, direccion VARCHAR(255)
					, fecha_alta DATE);

DROP TABLE usuario_rol;
CREATE TABLE usuario_rol(id_usuario_rol INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1) PRIMARY KEY
                              ,id_usuario INTEGER
                              ,id_rol INTEGER);	
                              
DROP TABLE rol_aplicacion;
CREATE TABLE rol_aplicacion(id_rol_aplicacion INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1) PRIMARY KEY
                              ,id_rol INTEGER
                              ,id_aplicacion INTEGER);	
					
